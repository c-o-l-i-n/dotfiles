-- center cursor on page after scrolling with <C-d> and <C-u>
vim.keymap.set("n", "<C-d>", "<C-d>zz")
vim.keymap.set("n", "<C-u>", "<C-u>zz")

-- exit input mode with jj to avoid reacing for the <Esc> key that's soooo far away
vim.keymap.set("i", "jj", "<Esc>")
